metros = float(input('digite um valor em metros:'))

centimetro = metros * 100
milimetro = metros * 1000

print(f"{metros} metros é igual a {centimetro:.2f} centimetros.")
print(f"{milimetro} milimetro é igual a {milimetro:.2f} milimetro.")
